
public class Conversao {
	public static void main(String[] args) {
		double salario = 1270.5;   // double é uma variavel que recebe um valor do tipo flutuante 
		int valor = (int) salario; // converter um valor flutuante em inteiro, temos que inserir (int) na variavel flutuante.
		System.out.println(" Meu salário é " + valor);
		
		
		long numeroGrande = 32323200932392L; // variavel long é para numeros grandes.
		
		System.out.println(numeroGrande);
	}
}
